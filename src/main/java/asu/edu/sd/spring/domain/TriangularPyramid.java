package asu.edu.sd.spring.domain;

public class TriangularPyramid {
	double sideOfTriangularBase;
	double volume;
	double height;
	String unit;
	public double getSideOfTriangularBase() {
		return sideOfTriangularBase;
	}
	public void setSideOfTriangularBase(double sideOfTriangularBase) {
		this.sideOfTriangularBase = sideOfTriangularBase;
	}
	public double getVolume() {
		return volume;
	}
	public void setVolume(double volume) {
		this.volume = volume;
	}
	public double getHeight() {
		return height;
	}
	public void setHeight(double height) {
		this.height = height;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
}
